package com.javalogicalstatements1.loops1;

public class WhileLoop {

	public static void main(String[] args) throws InterruptedException {
		System.out.println("Main method started ! ");
		int i = 1;
		while (10 >= 1) {
			System.out.println("Hello");
			i++;
			Thread.sleep(200);

		}

	}

}
